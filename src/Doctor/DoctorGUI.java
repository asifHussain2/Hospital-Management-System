    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
           try{
         String query = "select count(id) from doctor";
        PreparedStatement ps = DB.con.prepareStatement(query);
       ResultSet rs= ps.executeQuery();
       if(rs.next()){
            if(rs.getString(1).equalsIgnoreCase("30")){
                JOptionPane.showMessageDialog(null,"Maximum doctors are available in Hospital");
            }
            else{
          String name = name1.getText();
        String specialization = specialization1.getText();
        String degree = degree1.getText();
        String phone = phone1.getText();
        Date date = datechooser1.getDate();
        Doctor d = new Doctor(0,name,specialization,degree,phone,date);
        DoctorController.insertData(d);
        loadTable();
            }
       }
        
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null,""+e);
        }
      
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
       DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        int row = jTable1.getSelectedRow();
        if(row<0){
            JOptionPane.showMessageDialog(null,"Please select row.");
        }
        else{
            name1.setText((String) model.getValueAt(row, 1));
            specialization1.setText((String) model.getValueAt(row, 2));
            degree1.setText((String) model.getValueAt(row, 3));
            phone1.setText((String) model.getValueAt(row, 4));
            datechooser1.setDate((Date) model.getValueAt(row, 5));
        }
    }//GEN-LAST:event_jTable1MouseClicked

