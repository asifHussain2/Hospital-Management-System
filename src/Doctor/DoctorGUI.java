    public void addRowsToTable(List<Doctor> list){
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0);
        for(Doctor d:list){
            Vector v = new Vector();
            v.add(d.getId());
            v.add(d.getName());
            v.add(d.getSpecialization());
            v.add(d.getDegree());
            v.add(d.getPhoneNo());
            v.add(d.getDateOfJoin());
            model.addRow(v);
        }
    }
    
    public void loadTable(){
        List<Doctor> list = DoctorController.getDoctorList();
        addRowsToTable(list);
    }

