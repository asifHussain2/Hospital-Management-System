    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        int row = jTable1.getSelectedRow();
        if(row<0){
            JOptionPane.showMessageDialog(null,"Please select row");
        }
        else{
           int roomNo = (int) model.getValueAt(row, 0);
           Room r=  RoomController.getRoomById(roomNo);
           if(r!=null){
               Integer room = r.getRoomNo();
               String rooms = room.toString();
               room1.setText(rooms);
               category1.setSelectedItem(r.getCategory());
               
               
           }
        }
        
        
    }//GEN-LAST:event_jTable1MouseClicked

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
         DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        int row = jTable1.getSelectedRow();
        if(row<0){
            JOptionPane.showMessageDialog(null,"Please select row");
        }
        else{
            int roomNo = (int) model.getValueAt(row, 0);
            Integer updatedRoomNo = Integer.parseInt(room1.getText());
            String category = (String) category1.getSelectedItem();
            Room r = new Room(0,updatedRoomNo,category);
            RoomController.updateById(roomNo,r);
            loadTable();
        }
    
        
    }//GEN-LAST:event_jButton3ActionPerformed

