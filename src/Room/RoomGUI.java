    public void addRowsToTable(List<Room> list){
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0);
        for(Room r:list){
            Vector v = new Vector();
            v.add(r.getRoomNo());
            v.add(r.getCategory());
            model.addRow(v);
        }
    }
    
    public void loadTable(){
        List<Room> list = RoomController.getRoomList();
        addRowsToTable(list);
        
    }
